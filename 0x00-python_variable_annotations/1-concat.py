#!/usr/bin/env python3

"""
Module: 1-concat
Contains a type-annotated function `concat` that takes two strings as arguments and returns their concatenated string.
"""


def concat(str1: str, str2: str) -> str:
    """
    Function: concat
    Takes two strings as arguments and returns their concatenated string.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenated string of str1 and str2.
    """
    return str1 + str2
